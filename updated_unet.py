device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model = Unet().to(device)

criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.AdamW(model.parameters(), lr= config.lr, weight_decay= 0.005)


train_loss_list = []

def dice_loss_calculation(logits, target, smooth=1.0):
  prob = torch.softmax(logits, dim=1)
  pred = prob[:, 1, :, :]

  if len(target.shape) == 4:
    target = target.squeeze(1)

  target = target.float()

  #dice calculation
  intersection = (pred * target).sum(dim=(1,2))
  dice_coeff = (2 * intersection + smooth) / (pred.sum(dim=(1,2)) + target.sum(dim=(1, 2)) +smooth)

  return 1 - dice_coeff.mean()

#Training loop
for epoch in range(1, config.num_epochs + 1):
  model.train()
  running_loss = 0.0
  time1 = time.time()

  for images, ground in train_loader:
    images = images.to(device)
    ground = ground.to(device)

    #Standardize ground
    cross_target = ground.squeeze(1).long()

    optimizer.zero_grad()
    outputs = model(images)

    cross_loss = criterion(outputs, cross_target)
    dice_loss = dice_loss_calculation(outputs, ground.squeeze(1))

    loss_total = cross_loss + dice_loss

    loss_total.backward()
    optimizer.step()

    running_loss += loss_total.item()

  avg_loss = running_loss / len(train_loader)
  train_loss_list.append(avg_loss)

  time2 = time.time()
  print('-----------------------------------')
  print('Epoch {}, total time {:.2f}'.format(epoch, time2 - time1))
  print('train_loss average', avg_loss)


model.eval()
