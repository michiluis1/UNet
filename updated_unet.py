class LesionSegmentation(Dataset):
    def __init__(self, root_dir, ground_dir, is_train = True):
        self.root_dir = root_dir
        self.ground_dir = ground_dir
        self.is_train = is_train

        self.files = [f for f in os.listdir(root_dir) if f.lower().endswith('.jpg')]
        self.files.sort()

        self.mean = [0.485, 0.456, 0.406]
        self.std = [0.229, 0.224, 0.225]

    def __len__(self):
        return len(self.files)

    def __getitem__(self, idx):
        raw_name = self.files[idx]
        filename = os.path.splitext(raw_name)[0]
        ground_name = filename + '_segmentation.png'
        img_path = os.path.join(self.root_dir, raw_name)
        ground_path = os.path.join(self.ground_dir, ground_name)

        img = cv2.imread(img_path)
        img = cv2.resize(img, (572 , 572), interpolation=cv2.INTER_LINEAR) #resizing to 572, 572 to keep compatible with og unet
        img = img.astype(np.float32) / 255.0

        ground = cv2.imread(ground_path, cv2.IMREAD_GRAYSCALE)
        ground = cv2.resize(ground, (572,572), interpolation=cv2.INTER_NEAREST)

        if self.is_train:
          # randomly rotate and flip to improve results
            nrotate = random.randint(0, 3)
            img = np.rot90(img, nrotate)
            ground = np.rot90(ground, nrotate)
            if random.random() > 0.5:
                img = np.fliplr(img)
                ground = np.fliplr(ground)

            img = img.copy()
            ground = ground.copy()

        for i in range(img.shape[2]):
            img[:, :, i] = (img[:, :, i] - self.mean[i]) / self.std[i]

        img = np.transpose(img, (2, 0, 1))
        #convert to tensor
        img_tensor = torch.from_numpy(img.copy()).float()
        ground_tensor = torch.from_numpy(ground.copy())
        ground_tensor = (ground_tensor > 127).float()
        ground_tensor = ground_tensor.unsqueeze(0)


        return img_tensor, ground_tensor

